package modulo_7;

public class Aula7_4_declarando_variavel {
	
	public static void main(String[] args) {
		
		boolean logica =true ; /* variavel do tipo logica recebe valores true ou false*/
		
		/* variavel do tipo numérica , recebe apenas números são 4 tipos com diferentes tamanhos
		 * byte, short, int, long */
		byte inteiro8bits = 8;
		short inteiro16bits = 16;
		int inteiro32bits = 32;
		long inteiro64bits = 64;
		
		/*Variável do tipo String recebe 1 ou mais caracteres dentro de aspas duplas*/
		String caractere = "string";
		
		/*Variável numérica do tipo double e float recebe números com virgula*/		
		float decimal8digitos = 0.12345678f;
		double dedcimal10digitos = 0.12345678910;
		
		/* variáveis do tipo "var" assumem qualquer tipo primitivo , após receber o valor o tipo
		 * não se altera mais*/
		var tomaForma = "RECEBE: NUMEROS, BOOLEAN, CARACTERES , ETC...." ;
		
		/*OBESERVAÇÃO:
		 * PARA DECLARAR VARIAVEIS A PRIMEIRA LETRA SEMPRE É MINÚSCULA, CASO EXISTA 2 OU MAIS NOMES 
		 * A PRIMEIRA LETRA DE CADA NOME SEMPRE SERÁ MAIÚSCULA O RESTO MINÚSCULA*/
	}
}
